public class Main {

    public static void main(String[] args) {
        // 创建CountDownLatch对象
        CountDownLatch countDownLatch = new CountDownLatch(1);
        // 创建多个线程并发执行
        for (int i = 0; i < 5; i++) {
            // 创建任务
            Task task = new Task(countDownLatch);
            // 创建线程
            Thread thread = new Thread(task, "线程 " + i + " 号");
            // 启动线程
            thread.start();
        }
        try {
            // 使主线程休眠3秒钟
            Thread.sleep(3000);
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
        // 计数器-1
        countDownLatch.countDown();
    }
}